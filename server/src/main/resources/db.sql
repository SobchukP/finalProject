DROP TABLE IF EXISTS Accounts;

CREATE TABLE Accounts (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  card VARCHAR(20) NOT NULL,
  pin VARCHAR(4) NOT NULL,
  balance int DEFAULT 0
);

